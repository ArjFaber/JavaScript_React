import { useState, useEffect } from "react";

const useFetch = () => {
  const [arrayOfObjects, setArrayOfObjects] = useState([]); // Use state to handle data
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState(null);

  const fetchData = async () => {
    setIsLoading(true);

    try {
      const dataSet = require('./Data/data.json');
      const data = Object.values(dataSet);
      setArrayOfObjects(data); // Update state with fetched data
    } catch (error) {
      setError(error);
      console.log(error);
    } finally {
      setIsLoading(false);
    }
  };

  useEffect(() => {
    fetchData();
  }, []);

  const refetch = () => {
    setIsLoading(true);
    fetchData();
  };

  return [arrayOfObjects, isLoading, error, refetch];
};

export default useFetch;
